#百鸡百钱
#公鸡5文钱一只，母鸡3文钱一只，小鸡3只一文钱， 用100文钱买一百只鸡,其中公鸡，母鸡，小鸡都必须要有，问公鸡，母鸡，小鸡要买多少只刚好凑足100文钱？
#x为公鸡，y为母鸡，z为小鸡，鸡的总数量是：x+y+z=100,购买的金额限制是：5x+3y+z/3=100
for x in range(1,20):
    for y in range(1,33):
        z = 100-x-y
        if z>0  and (5*x+3*y+z/3)==100:
            print ('公鸡数量:{}，母鸡数量:{}，小鸡数量:{}'.format(x,y,z))